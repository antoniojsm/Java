public class questao27 {
    public static void main(String[]args){
        int numero = 1;

        // Enquanto o número for menor ou igual a 10, o loop continua
        while (numero <= 10) {
            // Exibe o número ao quadrado
            System.out.println("Número: " + numero + " ao quadrado: " + (numero * numero));
            
            // Incrementa o número
            numero++;
        }
    }
}
